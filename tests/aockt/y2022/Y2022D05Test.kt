package aockt.y2022

import io.github.jadarma.aockt.test.AdventDay
import io.github.jadarma.aockt.test.AdventSpec

@AdventDay(2022, 5, "Supply Stacks")
class Y2022D05Test : AdventSpec<Y2022D05>({

    val exampleInput = """
            [D]    
        [N] [C]    
        [Z] [M] [P]
         1   2   3 

        move 1 from 2 to 1
        move 3 from 1 to 3
        move 2 from 2 to 1
        move 1 from 1 to 2
    """.trimIndent()

    partOne { exampleInput shouldOutput "CMZ" }
    partTwo { exampleInput shouldOutput "MCD" }
})
